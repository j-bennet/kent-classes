-module(recur).

%% API
-export([fib/1, pieces/1]).

%%%-------------------------------------------------------------------
%% Fibonacci
%%%-------------------------------------------------------------------
fib(0) -> 0;
fib(1) -> 1;
fib(X) -> fib(X - 1) + fib(X - 2).

%%%-------------------------------------------------------------------
%% Max number of pieces made by X cuts
%%%-------------------------------------------------------------------
pieces(0) -> 1;
pieces(1) -> 2;
pieces(X) -> pieces(X - 1) + X.

f(0.0) -> 1;
f(X) -> X.
